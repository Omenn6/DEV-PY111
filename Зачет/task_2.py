def counting_game(n: int, k: int) -> int:
    """
    Моделирование считалочки
    Возвращает номер последнего оставшегося человека (отсчет с 1)
    """
    if n <= 0 or k <= 0:
        return 0

    # Создаем круг людей с номерами от 1 до N
    people = list(range(1, n + 1))

    # Стартовая позиция для отсчета
    idx = 0

    # Удаляем людей по одному, пока не останется последний
    while len(people) > 1:
        # Находим индекс человека, который вылетает (учитываем движение по кругу)
        idx = (idx + k - 1) % len(people)
        # Человек выбывает
        people.pop(idx)

    return people[0]


